/**
 * PARALLAX SCROLL GALLERY - For Fusion East Image Gallery
 *
 * This component creates a 3-column parallax image grid where:
 * - Left column: translates up-left + rotates counterclockwise on scroll
 * - Center column: stays static (anchor point)
 * - Right column: translates up-right + rotates clockwise on scroll
 * - Creates a dramatic "fanning out" effect as you scroll
 *
 * USAGE FOR FUSION EAST:
 * - Replace demo Unsplash images with actual restaurant photos
 * - Food photography, bar shots, interior, events, plating
 * - Use for: Restaurant section gallery, About page, or dedicated Gallery page
 * - All images from current website get converted to this animation
 *
 * PROPS:
 * - images: string[] — array of image URLs
 * - className: string — optional additional classes
 *
 * DEPENDENCIES:
 * - framer-motion
 * - next/image
 * - @/lib/utils (cn helper from shadcn)
 *
 * CUSTOMIZATION FOR FUSION EAST:
 * - Adjust h-[40rem] for taller/shorter scroll area
 * - Change gap-10 for tighter/wider image spacing
 * - Adjust translate values (-200) for more/less dramatic parallax
 * - Add dark overlay or border styling to images for dark theme
 * - Consider adding rounded-lg border border-[#333] for dark theme cards
 */

export function ParallaxScrollSecondDemo() {
  return <ParallaxScrollSecond images={images} />;
}

